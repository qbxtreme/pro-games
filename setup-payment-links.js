require("dotenv").config();

const fs = require("fs");
const path = require("path");
const Stripe = require("stripe");

const BASE_URL = process.env.BASE_URL || "http://localhost:8080";
const STRIPE_PACKS = JSON.parse(
  fs.readFileSync(path.join(__dirname, "stripe-packs.json"), "utf8")
);

async function main() {
  const key = process.env.STRIPE_SECRET_KEY;
  if (!key) {
    console.error("Missing STRIPE_SECRET_KEY in .env — get one at https://dashboard.stripe.com/test/apikeys");
    process.exit(1);
  }

  const stripe = new Stripe(key);
  const links = {};

  for (const [packId, pack] of Object.entries(STRIPE_PACKS)) {
    if (!pack.stripePriceId) {
      console.error(`Missing stripePriceId for ${packId} in stripe-packs.json`);
      process.exit(1);
    }

    const paymentLink = await stripe.paymentLinks.create({
      line_items: [{ price: pack.stripePriceId, quantity: 1 }],
      after_completion: {
        type: "redirect",
        redirect: {
          url: `${BASE_URL}/?purchase=success&pack=${packId}&session_id={CHECKOUT_SESSION_ID}`,
        },
      },
    });

    links[packId] = paymentLink.url;
    console.log(`${packId}: ${paymentLink.url}`);
  }

  const out = `// Auto-generated by npm run setup-payments
// Real Stripe Payment Links — redirect back after paying.

window.BECOME_A_PRO_PAYMENTS = {
  useApi: false,
  pack100: "${links.pack100}",
  pack500: "${links.pack500}",
  pack1200: "${links.pack1200}",
  pack3000: "${links.pack3000}",
};
`;

  fs.writeFileSync(path.join(__dirname, "payments-config.js"), out);
  console.log("\nWrote payments-config.js with live payment links.");
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
